            return;

        _searchTextByBranch[branch] = normalized;
        _listScrollByBranch[branch] = Vector2.Zero;
        _detailScrollByBranch[branch] = Vector2.Zero;
        PopulateBranch(GetBranchContainer(branch), branch);
    }

