    private string GetSearchText(PersistentCraftBranch branch)
    {
        return _searchTextByBranch.TryGetValue(branch, out var text) ? text : string.Empty;
    }

    private bool GetCraftableOnly(PersistentCraftBranch branch)
    {
        return _craftableOnlyByBranch.TryGetValue(branch, out var value) && value;
    }

    private List<PersistentCraftRecipePrototype> ApplyRecipeSearch(
        IReadOnlyList<PersistentCraftRecipePrototype> recipes,
        string searchText)
    {
        if (string.IsNullOrWhiteSpace(searchText))
            return recipes.ToList();

        return recipes.Where(recipe => MatchesSearch(recipe, searchText)).ToList();
    }

    private List<PersistentCraftRecipePrototype> ApplyCraftableFilter(
        IReadOnlyList<PersistentCraftRecipePrototype> recipes,
        bool craftableOnly)
    {
        if (!craftableOnly)
            return recipes.ToList();

        return recipes.Where(HasLocalMaterials).ToList();
    }

    private bool MatchesSearch(PersistentCraftRecipePrototype recipe, string searchText)
    {
        var query = searchText.Trim().ToLowerInvariant();
        if (string.IsNullOrWhiteSpace(query))
            return true;

        return ResolveRecipeName(recipe).ToLowerInvariant().Contains(query) ||
               GetRecipeCategoryPath(recipe).ToLowerInvariant().Contains(query) ||
               recipe.Tier.ToString().Contains(query) ||
               PersistentCraftingHelper.GetTierDisplayLabel(recipe.Tier).ToLowerInvariant().Contains(query) ||
               ResolveRecipeDescription(recipe).ToLowerInvariant().Contains(query);
    }

    private BoxContainer GetBranchContainer(PersistentCraftBranch branch) => branch switch
    {
        PersistentCraftBranch.Armor => ArmorBranch,
        PersistentCraftBranch.Anomaly => AnomalyBranch,
        _ => WeaponBranch,
    };

    private PersistentCraftBranch GetCurrentBranch() => Branches.CurrentTab switch
    {
        1 => PersistentCraftBranch.Armor,
        2 => PersistentCraftBranch.Anomaly,
        _ => PersistentCraftBranch.Weapon,
    };

    private PersistentCraftRecipePrototype ResolveSelectedRecipe(
        PersistentCraftBranch branch,
        IReadOnlyList<PersistentCraftRecipePrototype> recipes)
    {
        if (_selectedRecipes.TryGetValue(branch, out var selectedId))
        {
            var existing = recipes.FirstOrDefault(recipe => recipe.ID == selectedId);
            if (existing != null)
                return existing;
        }

        var fallback = _state == null
            ? recipes[0]
            : recipes.FirstOrDefault(recipe => HasRequirement(_state, recipe)) ?? recipes[0];

        _selectedRecipes[branch] = fallback.ID;
        return fallback;
    }

    private int GetSelectedTierFilter(
        PersistentCraftBranch branch,
        IReadOnlyList<PersistentCraftRecipePrototype> recipes,
        PersistentCraftBranchState branchState)
    {
        if (_selectedTierFilters.TryGetValue(branch, out var selectedTier) &&
            (selectedTier == 0 || recipes.Any(recipe => recipe.Tier == selectedTier)))
        {
            return selectedTier;
        }

        var preferredTier = 0;
        if (_state?.Loaded == true)
        {
            preferredTier = recipes
                .Where(recipe => HasRequirement(_state, recipe))
                .Select(recipe => recipe.Tier)
                .DefaultIfEmpty(recipes.Max(recipe => recipe.Tier))
                .Max();
        }

        _selectedTierFilters[branch] = preferredTier;
        return preferredTier;
    }

    private bool TryGetRecipeTexture(PersistentCraftRecipePrototype recipe, out Texture? texture)
    {
        texture = null;
        var displayProto = PersistentCraftingHelper.GetDisplayPrototypeId(recipe);
        if (string.IsNullOrWhiteSpace(displayProto))
            return false;

        try
        {
            var spriteSystem = _entityManager.EntitySysManager.GetEntitySystem<SpriteSystem>();
            texture = spriteSystem.GetPrototypeIcon(displayProto).Default;
            return texture != null;
        }
        catch
        {
            texture = null;
            return false;
        }
    }

    private bool HasRequirement(PersistentCraftState state, PersistentCraftRecipePrototype recipe)
    {
        return HasNodeUnlockedOrAutoAvailable(state, recipe.RequiredNode);
    }

    private string GetRequirementText(PersistentCraftRecipePrototype recipe)
    {
        if (TryResolveRequirementNode(recipe, out var node))
        {
            return ResolveNodeName(node);
        }

        return $"{Loc.GetString(PersistentCraftingHelper.GetBranchLocKey(recipe.Branch))} {PersistentCraftingHelper.GetTierDisplayLabel(recipe.Tier)}";
    }

    private bool CanViewRecipe(PersistentCraftRecipePrototype recipe)
    {
        return _state != null && HasRequirement(_state, recipe);
    }

    private Color GetRecipeMetaColor(PersistentCraftRecipePrototype recipe)
    {
        var loaded = _state?.Loaded == true;
        var requirementMet = _state != null && HasRequirement(_state, recipe);
        var hasMaterials = HasLocalMaterials(recipe);
        var canCraft = loaded && requirementMet && hasMaterials;
        var accent = GetAccent(recipe.Branch);

        return !requirementMet
            ? MissingColor
            : canCraft
                ? accent
                : hasMaterials
                    ? MutedText
                    : MissingColor;
    }

    private string BuildRecipeSecondaryLine(PersistentCraftRecipePrototype recipe)
    {
        var category = GetSubCategoryName(GetRecipeSubCategoryId(recipe));
        if (string.IsNullOrWhiteSpace(category))
            category = GetCategoryName(GetRecipeCategoryId(recipe));

        return $"{PersistentCraftingHelper.GetTierDisplayLabel(recipe.Tier)} | {category}";
    }

