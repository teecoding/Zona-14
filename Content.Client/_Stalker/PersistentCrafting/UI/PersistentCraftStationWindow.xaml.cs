    public PersistentCraftStationWindow(PersistentCraftClientPrototypeCache prototypeCache) : this()
    {
        _prototypeCache = prototypeCache;
        _recipeIndex = PersistentCraftRecipeIndex.Create(prototypeCache);
        _branchRegistry = prototypeCache.BranchRegistry;
        _branchCoordinator.SetBranchRegistry(_branchRegistry);

        var tempResolver = new PersistentCraftTextResolver(_prototype, _branchRegistry, PersistentCraftRecipeMetadataIndex.Empty);
        _recipeMetadataIndex = PersistentCraftRecipeMetadataIndex.Build(
            prototypeCache.AllRecipes,
            recipe => tempResolver.ResolveRecipeName(recipe),
            recipe => tempResolver.ResolveRecipeDescription(recipe),
            recipe => tempResolver.ComputeRecipeCategoryId(recipe),
            recipe => tempResolver.ComputeRecipeSubCategoryId(recipe),
            (categoryId, subCategoryId) => tempResolver.ResolveCategoryPath(categoryId, subCategoryId),
            PersistentCraftingHelper.GetTierDisplayLabel);
        _textResolver = new PersistentCraftTextResolver(_prototype, _branchRegistry, _recipeMetadataIndex);

        InitializeBranchContainers();
    }

    public void UpdateState(PersistentCraftState state, PersistentCraftClientPrototypeCache prototypeCache)
    {
        if (!ReferenceEquals(_prototypeCache, prototypeCache))
        {
            _prototypeCache = prototypeCache;
            _recipeIndex = PersistentCraftRecipeIndex.Create(prototypeCache);
            _branchRegistry = prototypeCache.BranchRegistry;
            _branchCoordinator.SetBranchRegistry(_branchRegistry);
            var tempResolver = new PersistentCraftTextResolver(_prototype, _branchRegistry, PersistentCraftRecipeMetadataIndex.Empty);
            _recipeMetadataIndex = PersistentCraftRecipeMetadataIndex.Build(
                prototypeCache.AllRecipes,
                recipe => tempResolver.ResolveRecipeName(recipe),
                recipe => tempResolver.ResolveRecipeDescription(recipe),
                recipe => tempResolver.ComputeRecipeCategoryId(recipe),
                recipe => tempResolver.ComputeRecipeSubCategoryId(recipe),
                (categoryId, subCategoryId) => tempResolver.ResolveCategoryPath(categoryId, subCategoryId),
                PersistentCraftingHelper.GetTierDisplayLabel);
            _textResolver = new PersistentCraftTextResolver(_prototype, _branchRegistry, _recipeMetadataIndex);
            InitializeBranchContainers();
        }

        _state = state;
        _recipes = prototypeCache.AllRecipes;
        _branchCoordinator.RebuildBranchStateIndex(state);
        _inventorySnapshot = BuildInventorySnapshot(_recipes);

        if (state.Loaded && _viewModel.SelectPreferredBranchOnNextUpdate)
        {
            _branchCoordinator.SelectPreferredBranchTab(Branches, branch => GetAvailableRecipeCount(state, branch));
            _viewModel.SelectPreferredBranchOnNextUpdate = false;
        }

        Render();
    }

    public void ResetInitialTabSelection()
    {
        _viewModel.SelectPreferredBranchOnNextUpdate = true;
    }

    public void RefreshLocalInventory()
    {
        if (_state == null)
            return;

        var newSnapshot = BuildInventorySnapshot(_recipes);
        if (newSnapshot.Signature == _inventorySnapshot.Signature)
            return;

        _inventorySnapshot = newSnapshot;

        // Rebuild only the currently visible branch to avoid flickering
        var branch = GetCurrentBranch();
        PopulateBranch(GetBranchContainer(branch), branch);
        _viewModel.LastVisibleBranch = branch;
    }

    public void NotifyCraftStarted(string recipeId, float durationSeconds)
    {
        _activeCraftRecipeId = recipeId;
        _activeCraftDurationSeconds = MathF.Max(0.05f, durationSeconds);
        _activeCraftStartedAt = _timing.CurTime;

        if (string.Equals(_activeCraftResultRecipeId, recipeId, StringComparison.Ordinal))
            _activeCraftResultRecipeId = null;

        UpdateActiveRecipeIconCraftVisuals();
    }

    public void NotifyCraftFinished(string recipeId, PersistentCraftRecipeExecutionResult result)
    {
        if (string.Equals(_activeCraftRecipeId, recipeId, StringComparison.Ordinal))
        {
            _activeCraftRecipeId = null;
            _activeCraftDurationSeconds = 0f;
        }

        _activeCraftResultRecipeId = recipeId;
        _activeCraftResultColor = result == PersistentCraftRecipeExecutionResult.Completed
            ? PersistentCraftUiTheme.Success
            : PersistentCraftUiTheme.Danger;
        _activeCraftResultAt = _timing.CurTime;

        UpdateActiveRecipeIconCraftVisuals();
    }

    protected override void FrameUpdate(FrameEventArgs args)
    {
        base.FrameUpdate(args);

        if (_pendingScrollRestoreBranches.Count > 0)
        {
            foreach (var branch in _pendingScrollRestoreBranches)
            {
                if (_activeListScrollByBranch.TryGetValue(branch, out var listScroll) &&
                    _viewModel.ListScrollByBranch.TryGetValue(branch, out var listScrollValue))
                {
                    listScroll.SetScrollValue(listScrollValue);
                }

                if (_activeDetailScrollByBranch.TryGetValue(branch, out var detailScroll) &&
                    _viewModel.DetailScrollByBranch.TryGetValue(branch, out var detailScrollValue))
                {
                    detailScroll.SetScrollValue(detailScrollValue);
                }
            }

            _pendingScrollRestoreBranches.Clear();
        }

        UpdateActiveRecipeIconCraftVisuals();
    }

    private void Render()
    {
        if (_state == null)
            return;

        var branch = GetCurrentBranch();
        PopulateBranch(GetBranchContainer(branch), branch);
        _viewModel.LastVisibleBranch = branch;
    }

    private void PopulateBranch(BoxContainer container, string branch)
    {
        var restoreSearchFocus = false;
        var restoreSearchCursor = 0;
        var restoreSearchSelection = 0;

        if (_activeSearchInputsByBranch.TryGetValue(branch, out var previousSearchInput) &&
            previousSearchInput.HasKeyboardFocus())
        {
            restoreSearchFocus = true;
            restoreSearchCursor = previousSearchInput.CursorPosition;
            restoreSearchSelection = previousSearchInput.SelectionStart;
        }

        RememberBranchScroll(branch);
        container.RemoveAllChildren();
        _activeListScrollByBranch.Remove(branch);
        _activeDetailScrollByBranch.Remove(branch);
        _activeSearchInputsByBranch.Remove(branch);
        _detailContentHostsByBranch.Remove(branch);
        _recipeEntryControlsByBranch.Remove(branch);
        _activeRecipeIconOverlay = null;
        _activeRecipeIconRecipeId = null;
        _activeRecipeProgressColor = PersistentCraftUiTheme.Selection;

        if (_state == null)
            return;

        var branchRecipes = GetRecipesForBranch(branch);

        if (branchRecipes.Count == 0)
        {
            container.AddChild(new Label
            {
                Text = Loc.GetString("persistent-craft-station-no-recipes"),
                FontColorOverride = MutedText,
            });
            return;
        }

        var branchState = GetBranchState(branch);
        _visibleBranchStatesByBranch[branch] = branchState;
        _recipeEntryControlsByBranch[branch] = new Dictionary<string, RecipeEntryControls>();

        if (!_state.Loaded)
        {
            container.AddChild(CreateBranchHeader(branch, branchState, 0, branchRecipes.Count));
            container.AddChild(new Control { MinSize = new Vector2(1, 12) });
            container.AddChild(new Label
            {
                Text = Loc.GetString("persistent-craft-window-loading-points"),
                FontColorOverride = MutedText,
            });
            return;
        }

        var branchData = PersistentCraftStationBranchBuilder.Build(
            branch,
            branchRecipes,
            _state,
            _viewModel,
            recipe => HasRequirement(_state, recipe),
            HasLocalMaterials,
            (recipe, query) => _textResolver.MatchesRecipeSearch(recipe, query));

        container.AddChild(CreateBranchHeader(branch, branchState, branchData.UnlockedRecipes.Count, branchRecipes.Count));
        container.AddChild(new Control { MinSize = new Vector2(1, 12) });

        if (branchData.UnlockedRecipes.Count == 0)
        {
            container.AddChild(new Label
            {
                Text = Loc.GetString("persistent-craft-station-no-recipes"),
                FontColorOverride = MutedText,
            });
            return;
        }

        _recipeCraftabilityById.Clear();
        foreach (var entry in branchData.CraftabilityByRecipeId)
        {
            _recipeCraftabilityById[entry.Key] = entry.Value;
        }

        var workspace = new PersistentCraftRecipeWorkspace();
        workspace.SetAccent(GetAccent(branch));
        workspace.FilterContainer.AddChild(CreateFilterToolbar(branch, branchData.SearchText, branchData.CraftableOnly));
        workspace.TierFilterContainer.AddChild(CreateTierFilterRow(branch, branchData.UnlockedRecipes, branchData.SelectedTier, GetAccent(branch)));
        workspace.SummaryContainer.AddChild(CreateRecipeListSummary(branchData.SelectedTier, branchData.VisibleRecipes.Count, branchData.CraftableCount, branchData.CraftableOnly, GetAccent(branch)));
        var listScroll = workspace.RecipeListScroll;

        if (branchData.VisibleRecipes.Count > 0 && branchData.SelectedRecipe != null)
        {
            workspace.ListContainer.AddChild(CreateRecipeList(branch, branchData.VisibleRecipes, branchData.SelectedRecipe, GetAccent(branch)));
            workspace.DetailContainer.AddChild(CreateRecipeDetailsPanel(branchData.SelectedRecipe, branchState));
            _detailContentHostsByBranch[branch] = workspace.DetailContainer;
        }
        else
        {
            workspace.ListContainer.AddChild(CreateEmptyRecipeListMessage(branchData.SearchText, branchData.CraftableOnly));
            workspace.DetailContainer.AddChild(CreateEmptyDetailPanel(branchData.SearchText, branchData.CraftableOnly));
            _detailContentHostsByBranch[branch] = workspace.DetailContainer;
        }

        _activeListScrollByBranch[branch] = listScroll;
        _activeDetailScrollByBranch[branch] = workspace.RecipeDetailScroll;
        container.AddChild(workspace);
        RestoreBranchScroll(branch, listScroll, workspace.RecipeDetailScroll);
        RestoreSearchInputFocus(branch, restoreSearchFocus, restoreSearchCursor, restoreSearchSelection);
    }

    private void RestoreSearchInputFocus(
        string branch,
        bool shouldRestoreFocus,
        int cursorPosition,
        int selectionStart)
    {
        if (!shouldRestoreFocus ||
            !_activeSearchInputsByBranch.TryGetValue(branch, out var refreshedInput))
        {
            return;
        }

        refreshedInput.GrabKeyboardFocus();
        refreshedInput.CursorPosition = Math.Clamp(cursorPosition, 0, refreshedInput.Text.Length);
        refreshedInput.SelectionStart = Math.Clamp(selectionStart, 0, refreshedInput.Text.Length);
    }

    private void BindActiveRecipeIconOverlay(
        string recipeId,
        Color progressColor,
        PersistentCraftRecipeIconProgressOverlay overlay)
    {
        _activeRecipeIconOverlay = overlay;
        _activeRecipeIconRecipeId = recipeId;
        _activeRecipeProgressColor = progressColor;
        UpdateActiveRecipeIconCraftVisuals();
    }

    private void UpdateActiveRecipeIconCraftVisuals()
    {
        if (_activeRecipeIconOverlay == null || string.IsNullOrWhiteSpace(_activeRecipeIconRecipeId))
            return;

        float? progressRatio = null;
        if (string.Equals(_activeCraftRecipeId, _activeRecipeIconRecipeId, StringComparison.Ordinal) &&
            _activeCraftDurationSeconds > 0f)
        {
            var elapsed = (float) (_timing.CurTime - _activeCraftStartedAt).TotalSeconds;
            progressRatio = Math.Clamp(elapsed / _activeCraftDurationSeconds, 0f, 1f);
        }

        var flashAlpha = 0f;
        var flashColor = Color.Transparent;
        if (string.Equals(_activeCraftResultRecipeId, _activeRecipeIconRecipeId, StringComparison.Ordinal))
        {
            var flashElapsed = (float) (_timing.CurTime - _activeCraftResultAt).TotalSeconds;
            if (flashElapsed <= RecipeResultFlashDuration)
            {
                var fade = 1f - flashElapsed / RecipeResultFlashDuration;
                flashColor = _activeCraftResultColor;
                flashAlpha = 0.72f * Math.Clamp(fade, 0f, 1f);
            }
            else
            {
                _activeCraftResultRecipeId = null;
            }
        }

        _activeRecipeIconOverlay.SetProgress(progressRatio, _activeRecipeProgressColor);
        _activeRecipeIconOverlay.SetFlash(flashColor, flashAlpha);
    }

    private bool TryGetRecipeTexture(PersistentCraftRecipePrototype recipe, out Texture? texture)
    {
        texture = null;
        var displayProto = PersistentCraftingHelper.GetDisplayPrototypeId(recipe);
        if (string.IsNullOrWhiteSpace(displayProto))
            return false;

        try
        {
            var spriteSystem = _entityManager.EntitySysManager.GetEntitySystem<SpriteSystem>();
            texture = spriteSystem.GetPrototypeIcon(displayProto).Default;
            return texture != null;
        }
        catch (Exception ex)
        {
            Sawmill.Warning($"Failed to resolve recipe texture for recipe '{recipe.ID}' using proto '{displayProto}': {ex}");
            texture = null;
            return false;
        }
    }

    private bool HasRequirement(PersistentCraftState state, PersistentCraftRecipePrototype recipe)
    {
        return HasNodeUnlockedOrAutoAvailable(state, recipe.RequiredNode);
    }

    private string GetRequirementText(PersistentCraftRecipePrototype recipe)
    {
        if (TryResolveRequirementNode(recipe, out var node))
        {
            return _textResolver.ResolveNodeName(node);
        }

        return $"{ResolveBranchTitle(recipe.Branch)} {PersistentCraftingHelper.GetTierDisplayLabel(recipe.Tier)}";
    }

    private bool CanViewRecipe(PersistentCraftRecipePrototype recipe)
    {
        return _state != null && HasRequirement(_state, recipe);
    }

    private Color GetRecipeMetaColor(PersistentCraftRecipePrototype recipe)
    {
        var loaded = _state?.Loaded == true;
        var requirementMet = _state != null && HasRequirement(_state, recipe);
        var hasMaterials = GetHasLocalMaterials(recipe);
        var canCraft = loaded && requirementMet && hasMaterials;
        var accent = GetAccent(recipe.Branch);

        return !requirementMet
            ? MissingColor
            : canCraft
                ? accent
                : hasMaterials
                    ? MutedText
                    : MissingColor;
    }

