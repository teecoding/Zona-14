    private string BuildHeaderInfoMarkup(PersistentCraftRecipePrototype recipe)
    {
        var resultText = recipe.Results.Count == 0
            ? Loc.GetString("persistent-craft-none")
            : FormatResult(recipe.Results[0]);

        return $"[color={DescriptionText.ToHex()}]{FormattedMessage.EscapeText(resultText)}[/color]\n" +
               $"[color={MutedText.ToHex()}]{Loc.GetString("persistent-craft-recipe-time")}: {GetEffectiveCraftTime(recipe):0.#} {Loc.GetString("persistent-craft-recipe-seconds")}[/color]";
    }

    private string BuildRequirementMarkup(PersistentCraftRecipePrototype recipe)
    {
        if (TryResolveRequirementNode(recipe, out var node))
        {
            var lines = new List<string>
            {
                $"[color={DescriptionText.ToHex()}]- {FormattedMessage.EscapeText(ResolveNodeName(node))}[/color]"
            };

            return string.Join("\n", lines);
        }

        return $"[color={DescriptionText.ToHex()}]- {FormattedMessage.EscapeText(GetRequirementText(recipe))}[/color]";
    }


    private bool TryResolveRequirementNode(PersistentCraftRecipePrototype recipe, out PersistentCraftNodePrototype node)
    {
        if (TryGetNodePrototype(recipe.RequiredNode, out var resolvedNode))
        {
            node = resolvedNode;
            return true;
        }

        node = default!;
        return false;
    }

    private int GetAvailableRecipeCount(PersistentCraftState state, string branch)
    {
        var recipes = GetRecipesForBranch(branch);
        var availableRecipeCount = 0;

        for (var i = 0; i < recipes.Count; i++)
        {
            if (HasRequirement(state, recipes[i]))
                availableRecipeCount++;
        }

        return availableRecipeCount;
    }

    private string GetRecipeCategoryId(PersistentCraftRecipePrototype recipe)
    {
        if (_recipeMetadataIndex.TryGetCategoryId(recipe.ID, out var categoryId))
            return categoryId;

        return ComputeRecipeCategoryId(recipe);
    }

    private string GetRecipeSubCategoryId(PersistentCraftRecipePrototype recipe)
    {
        if (_recipeMetadataIndex.TryGetSubCategoryId(recipe.ID, out var subCategoryId))
            return subCategoryId;

        return ComputeRecipeSubCategoryId(recipe);
    }

    private string ComputeRecipeCategoryId(PersistentCraftRecipePrototype recipe)
    {
        if (!string.IsNullOrWhiteSpace(recipe.Category))
            return recipe.Category!;

        return _branchRegistry.TryGetBranchDefinition(recipe.Branch, out var definition)
            ? definition.DefaultCategory
            : string.Empty;
    }

    private string ComputeRecipeSubCategoryId(PersistentCraftRecipePrototype recipe)
    {
        if (!string.IsNullOrWhiteSpace(recipe.SubCategory))
            return recipe.SubCategory!;

        return _branchRegistry.TryGetBranchDefinition(recipe.Branch, out var definition) &&
               definition.UseTierSubcategories
            ? $"tier-{recipe.Tier}"
            : string.Empty;
    }

    private string ResolveCategoryPath(string categoryId, string subCategoryId)
    {
        var categoryName = GetCategoryName(categoryId);
        var subCategoryName = GetSubCategoryName(subCategoryId);
        return string.IsNullOrWhiteSpace(subCategoryName)
            ? categoryName
            : $"{categoryName} / {subCategoryName}";
    }

    private PersistentCraftCategoryPrototype? GetCategoryPrototype(string categoryId)
    {
        return _prototype.TryIndex<PersistentCraftCategoryPrototype>(categoryId, out var prototype)
            ? prototype
            : null;
    }

    private PersistentCraftSubCategoryPrototype? GetSubCategoryPrototype(string subCategoryId)
    {
        return _prototype.TryIndex<PersistentCraftSubCategoryPrototype>(subCategoryId, out var prototype)
            ? prototype
            : null;
    }

    private string GetCategoryName(string categoryId)
    {
        if (GetCategoryPrototype(categoryId) is { } category)
            return TryLoc(category.Name) ?? category.Name;

        var locKey = $"persistent-craft-category-{categoryId}";
        return TryLoc(locKey) ?? HumanizeIdentifier(categoryId);
    }

    private string GetSubCategoryName(string subCategoryId)
    {
        if (string.IsNullOrWhiteSpace(subCategoryId))
            return string.Empty;

        if (GetSubCategoryPrototype(subCategoryId) is { } subCategory)
            return TryLoc(subCategory.Name) ?? subCategory.Name;

        if (subCategoryId.StartsWith("tier-") &&
            subCategoryId.Length > "tier-".Length &&
            int.TryParse(subCategoryId.Substring("tier-".Length), out var tier))
            return $"{Loc.GetString("persistent-craft-level-label")} {PersistentCraftingHelper.GetTierDisplayLabel(tier)}";

        var locKey = $"persistent-craft-subcategory-{subCategoryId}";
        return TryLoc(locKey) ?? HumanizeIdentifier(subCategoryId);
    }

    private int GetCategoryOrder(string categoryId)
    {
        return GetCategoryPrototype(categoryId)?.Order ?? 99;
    }

    private int GetSubCategoryOrder(string subCategoryId)
    {
        return GetSubCategoryPrototype(subCategoryId)?.Order
               ?? (subCategoryId.StartsWith("tier-") &&
                   subCategoryId.Length > "tier-".Length &&
                   int.TryParse(subCategoryId.Substring("tier-".Length), out var tier)
                   ? tier
                   : 99);
    }

    private static string? TryLoc(string locKey)
    {
        try
        {
            return Loc.GetString(locKey);
        }
        catch
        {
            return null;
        }
    }

    private static string HumanizeIdentifier(string value)
    {
        if (string.IsNullOrWhiteSpace(value))
            return string.Empty;

        var spaced = value.Replace('-', ' ').Replace('_', ' ').Trim();
        if (string.IsNullOrWhiteSpace(spaced))
            return string.Empty;

        return string.Concat(char.ToUpperInvariant(spaced[0]).ToString(), spaced.Substring(1));
    }

    private string GetRecipeCategoryPath(PersistentCraftRecipePrototype recipe)
    {
        if (_recipeMetadataIndex.TryGetCategoryPath(recipe.ID, out var categoryPath))
            return categoryPath;

        return ResolveCategoryPath(GetRecipeCategoryId(recipe), GetRecipeSubCategoryId(recipe));
    }

    private bool HasNodeUnlockedOrAutoAvailable(PersistentCraftState state, string nodeId)
    {
        return PersistentCraftNodeAvailabilityResolver.HasNodeUnlockedOrAutoAvailable(
            state,
            nodeId,
            ResolveNodePrototypeOrNull);
    }

    private string BuildIngredientMarkup(PersistentCraftRecipePrototype recipe)
    {
        var builder = new StringBuilder();
        for (var i = 0; i < recipe.Ingredients.Count; i++)
        {
            if (i > 0)
                builder.Append('\n');

            builder.Append(FormatIngredientLine(recipe, recipe.Ingredients[i]));
        }

        return builder.ToString();
    }

    private string BuildResultMarkup(PersistentCraftRecipePrototype recipe)
    {
        var builder = new StringBuilder();
        for (var i = 0; i < recipe.Results.Count; i++)
        {
            if (i > 0)
                builder.Append('\n');

            var result = recipe.Results[i];
            builder.Append($"[color={DescriptionText.ToHex()}]- {FormattedMessage.EscapeText(FormatResult(result))}[/color]");
        }

        return builder.ToString();
    }

    private string FormatIngredient(PersistentCraftIngredient ingredient)
    {
        var name = ingredient.GetSelectorKind() switch
        {
            PersistentCraftIngredientSelectorKind.Proto => ResolveEntityName(ingredient.Proto!),
            PersistentCraftIngredientSelectorKind.StackType => ingredient.StackType!,
            PersistentCraftIngredientSelectorKind.Tag => FormatTagIngredientName(ingredient.Tag!),
            _ => "?",
        };

        return $"{name} x{ingredient.Amount}";
    }

    private string FormatResult(PersistentCraftResult result)
    {
        return $"{ResolveEntityName(result.Proto)} x{result.Amount}";
    }

    private string FormatIngredientLine(PersistentCraftRecipePrototype recipe, PersistentCraftIngredient ingredient)
    {
        var requiredAmount = GetEffectiveIngredientAmount(recipe, ingredient);
        if (!IsLocallyVerifiableIngredient(ingredient))
        {
            return $"[color={MutedText.ToHex()}]- {FormattedMessage.EscapeText(FormatIngredient(ingredient))} (?/{requiredAmount})[/color]";
        }

        var owned = GetOwnedAmount(ingredient);
        var enough = owned >= requiredAmount;
        var color = enough ? EnoughColor : MissingColor;
        return $"[color={color.ToHex()}]- {FormattedMessage.EscapeText(FormatIngredient(ingredient))} ({owned}/{requiredAmount})[/color]";
    }

    private bool GetHasLocalMaterials(PersistentCraftRecipePrototype recipe)
    {
        if (_recipeCraftabilityById.TryGetValue(recipe.ID, out var cached))
            return cached;

        var hasMaterials = HasLocalMaterials(recipe);
        _recipeCraftabilityById[recipe.ID] = hasMaterials;
        return hasMaterials;
    }

    private bool HasLocalMaterials(PersistentCraftRecipePrototype recipe)
    {
        for (var i = 0; i < recipe.Ingredients.Count; i++)
        {
            var ingredient = recipe.Ingredients[i];
            if (!IsLocallyVerifiableIngredient(ingredient))
                continue;

            if (GetOwnedAmount(ingredient) < GetEffectiveIngredientAmount(recipe, ingredient))
                return false;
        }

        return true;
    }

    private static bool IsLocallyVerifiableIngredient(PersistentCraftIngredient ingredient)
    {
        return ingredient.GetSelectorKind() != PersistentCraftIngredientSelectorKind.None &&
               ingredient.GetSelectorKind() != PersistentCraftIngredientSelectorKind.InvalidMultiple;
    }

    private static string FormatTagIngredientName(string tag)
    {
        if (tag.StartsWith("STArtifactTier", StringComparison.Ordinal) &&
            int.TryParse(tag["STArtifactTier".Length..], out var tier))
        {
            return $"Артефакт T{tier}";
        }

        return $"#{tag}";
    }

    private int GetOwnedAmount(PersistentCraftIngredient ingredient)
    {
        return _inventorySnapshot.GetAmount(ingredient);
    }

    private string ResolveEntityName(string prototypeId)
    {
        return _prototype.TryIndex<EntityPrototype>(prototypeId, out var prototype)
            ? prototype.Name
            : prototypeId;
    }

    private string ResolveRecipeName(PersistentCraftRecipePrototype recipe)
    {
        if (_recipeMetadataIndex.TryGetName(recipe.ID, out var cached))
            return cached;

        return ResolveRecipeNameCore(recipe);
    }

    private string ResolveRecipeNameCore(PersistentCraftRecipePrototype recipe)
    {
        var displayProto = PersistentCraftingHelper.GetDisplayPrototypeId(recipe);
        if (!string.IsNullOrWhiteSpace(displayProto) &&
            _prototype.TryIndex<EntityPrototype>(displayProto, out var prototype) &&
            !string.IsNullOrWhiteSpace(prototype.Name))
        {
            return prototype.Name;
        }

        return TryLoc(recipe.Name) ?? recipe.Name;
    }

    private string ResolveRecipeDescription(PersistentCraftRecipePrototype recipe)
    {
        if (_recipeMetadataIndex.TryGetDescription(recipe.ID, out var cached))
            return cached;

        return ResolveRecipeDescriptionCore(recipe);
    }

    private string ResolveRecipeDescriptionCore(PersistentCraftRecipePrototype recipe)
    {
        var displayProto = PersistentCraftingHelper.GetDisplayPrototypeId(recipe);
        if (!string.IsNullOrWhiteSpace(displayProto) &&
            _prototype.TryIndex<EntityPrototype>(displayProto, out var prototype) &&
            !string.IsNullOrWhiteSpace(prototype.Description))
        {
            return prototype.Description;
        }

        return TryLoc(recipe.Description) ?? recipe.Description;
    }

    private string ResolveNodeName(PersistentCraftNodePrototype node)
    {
        if (!string.IsNullOrWhiteSpace(node.Name))
            return TryLoc(node.Name) ?? node.Name;

        if (!string.IsNullOrWhiteSpace(node.DisplayProto) &&
            _prototype.TryIndex<EntityPrototype>(node.DisplayProto, out var prototype) &&
            !string.IsNullOrWhiteSpace(prototype.Name))
        {
            return prototype.Name;
        }

        return TryLoc("persistent-craft-node-sub-recipe-name") ?? node.ID;
    }

    private PersistentCraftBranchState GetBranchState(string branch)
    {
        return _branchCoordinator.GetBranchState(branch);
    }


    private static string GetStatusKey(bool loaded, bool requirementMet, bool hasMaterials)
    {
        if (!loaded)
            return "persistent-craft-station-status-loading";

        if (!requirementMet)
            return "persistent-craft-station-status-locked";

        return hasMaterials
            ? "persistent-craft-station-status-available"
            : "persistent-craft-station-status-missing-materials";
    }

    private Color GetAccent(string branch)
    {
        return _branchRegistry.TryGetBranchDefinition(branch, out var definition)
            ? definition.AccentColor
            : Color.White;
    }

    private string ResolveBranchTitle(string branchId)
    {
        return _branchRegistry.TryGetBranchDefinition(branchId, out var definition)
            ? ResolveBranchTitle(definition)
            : branchId;
    }

    private static string ResolveBranchTitle(PersistentCraftBranchPrototype definition)
    {
        return TryLoc(definition.Name) ?? definition.Name;
    }

    private void InitializeBranchContainers()
    {
        Branches.RemoveAllChildren();
        _branchContainers.Clear();

        for (var index = 0; index < _branchRegistry.OrderedBranches.Count; index++)
        {
            var definition = _branchRegistry.OrderedBranches[index];
            var container = new BoxContainer
            {
                Orientation = BoxContainer.LayoutOrientation.Vertical,
                Margin = new Thickness(12, 12, 12, 12),
                HorizontalExpand = true,
                VerticalExpand = true,
            };

            Branches.AddChild(container);
            _branchContainers[definition.ID] = container;
            Branches.SetTabTitle(index, ResolveBranchTitle(definition));
        }

        _viewModel.LastVisibleBranch = _branchRegistry.FirstBranchId;
    }

    private IReadOnlyList<PersistentCraftRecipePrototype> GetRecipesForBranch(string branch)
    {
        if (_recipeIndex != null)
        {
            return _recipeIndex.GetByBranch(branch);
        }

        var recipes = new List<PersistentCraftRecipePrototype>();
        for (var i = 0; i < _recipes.Count; i++)
        {
            var recipe = _recipes[i];
            if (recipe.Branch == branch)
                recipes.Add(recipe);
        }

        return recipes;
    }

    private bool TryGetRecipeById(string recipeId, out PersistentCraftRecipePrototype recipe)
    {
        if (_recipeIndex != null &&
            _recipeIndex.TryGetById(recipeId, out recipe))
        {
            return true;
        }

        for (var i = 0; i < _recipes.Count; i++)
        {
            var item = _recipes[i];
            if (item.ID != recipeId)
                continue;

            recipe = item;
            return true;
        }

        recipe = default!;
        return false;
    }

    private bool TryGetNodePrototype(string nodeId, out PersistentCraftNodePrototype node)
    {
        if (_prototypeCache != null &&
            _prototypeCache.TryGetNode(nodeId, out node))
        {
            return true;
        }

        if (_prototype.TryIndex<PersistentCraftNodePrototype>(nodeId, out var resolvedNode) &&
            resolvedNode != null)
        {
            node = resolvedNode;
            return true;
        }

        node = default!;
        return false;
    }

    private PersistentCraftNodePrototype? ResolveNodePrototypeOrNull(string nodeId)
    {
        return TryGetNodePrototype(nodeId, out var node)
            ? node
            : null;
    }

    private PersistentCraftInventorySnapshot BuildInventorySnapshot(IReadOnlyList<PersistentCraftRecipePrototype> recipes)
    {
        if (_player.LocalEntity is not { Valid: true } user || !_entityManager.EntityExists(user))
            return PersistentCraftInventorySnapshot.Empty;

        var trackedIngredients = CollectTrackedIngredients(recipes);
        return PersistentCraftInventorySnapshot.Build(
            _entityManager,
            _tag,
            user,
            trackedIngredients);
    }

    private static List<PersistentCraftIngredient> CollectTrackedIngredients(IReadOnlyList<PersistentCraftRecipePrototype> recipes)
    {
        var tracked = new List<PersistentCraftIngredient>();
        var seen = new HashSet<string>();

        for (var i = 0; i < recipes.Count; i++)
        {
            var recipe = recipes[i];
            for (var j = 0; j < recipe.Ingredients.Count; j++)
            {
                var ingredient = recipe.Ingredients[j];
                var key = $"{(int) ingredient.GetSelectorKind()}|{ingredient.GetSelectorValue()}";
                if (seen.Add(key))
                    tracked.Add(ingredient);
            }
        }

        return tracked;
    }

    private float GetEffectiveCraftTime(PersistentCraftRecipePrototype recipe)
    {
        return MathF.Max(0.25f, recipe.CraftTime);
    }

    private int GetEffectiveIngredientAmount(PersistentCraftRecipePrototype recipe, PersistentCraftIngredient ingredient)
    {
        _ = recipe;
        return Math.Max(1, ingredient.Amount);
    }

    private int GetMaxCraftCount(PersistentCraftRecipePrototype recipe)
    {
        var maxCount = int.MaxValue;

        for (var i = 0; i < recipe.Ingredients.Count; i++)
        {
            var ingredient = recipe.Ingredients[i];
            if (!IsLocallyVerifiableIngredient(ingredient))
                return 1;

            var requiredAmount = GetEffectiveIngredientAmount(recipe, ingredient);
            if (requiredAmount <= 0)
                continue;

            var ownedAmount = GetOwnedAmount(ingredient);
            var craftsByIngredient = ownedAmount / requiredAmount;
            if (craftsByIngredient < maxCount)
                maxCount = craftsByIngredient;
        }

        if (maxCount == int.MaxValue)
            return 1;

        return Math.Max(0, maxCount);
    }
