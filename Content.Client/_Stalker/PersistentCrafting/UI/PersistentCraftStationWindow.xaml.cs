    private string BuildHeaderInfoMarkup(PersistentCraftRecipePrototype recipe)
    {
        var result = recipe.Results.FirstOrDefault();
        var resultText = result == null
            ? Loc.GetString("persistent-craft-none")
            : FormatResult(result);

        return $"[color={DescriptionText.ToHex()}]{FormattedMessage.EscapeText(resultText)}[/color]\n" +
               $"[color={MutedText.ToHex()}]{Loc.GetString("persistent-craft-recipe-time")}: {GetEffectiveCraftTime(recipe):0.#} {Loc.GetString("persistent-craft-recipe-seconds")}[/color]";
    }

    private string BuildRequirementMarkup(PersistentCraftRecipePrototype recipe)
    {
        if (TryResolveRequirementNode(recipe, out var node))
        {
            var lines = new List<string>
            {
                $"[color={DescriptionText.ToHex()}]- {FormattedMessage.EscapeText(ResolveNodeName(node))}[/color]"
            };

            return string.Join("\n", lines);
        }

        return $"[color={DescriptionText.ToHex()}]- {FormattedMessage.EscapeText(GetRequirementText(recipe))}[/color]";
    }


    private bool TryResolveRequirementNode(PersistentCraftRecipePrototype recipe, out PersistentCraftNodePrototype node)
    {
        PersistentCraftNodePrototype? resolvedNode;
        if (_prototype.TryIndex<PersistentCraftNodePrototype>(recipe.RequiredNode, out resolvedNode) &&
            resolvedNode != null)
        {
            node = resolvedNode;
            return true;
        }

        node = default!;
        return false;
    }

    private void SelectPreferredBranchTab()
    {
        if (_state == null)
            return;

        var preferred = PersistentCraftingHelper.EnumerateBranches()
            .Select(branch => new
            {
                Branch = branch,
                Recipes = _recipes.Count(recipe => recipe.Branch == branch && HasRequirement(_state, recipe)),
                Points = GetBranchState(_state, branch).AvailablePoints,
            })
            .OrderByDescending(entry => entry.Recipes)
            .ThenByDescending(entry => entry.Points)
            .FirstOrDefault(entry => entry.Recipes > 0 || entry.Points > 0);

        if (preferred != null)
            Branches.CurrentTab = GetBranchTabIndex(preferred.Branch);
    }

    private static int GetBranchTabIndex(PersistentCraftBranch branch)
    {
        return branch switch
        {
            PersistentCraftBranch.Weapon => 0,
            PersistentCraftBranch.Armor => 1,
            PersistentCraftBranch.Anomaly => 2,
            _ => 0,
        };
    }

    private string GetRecipeCategoryId(PersistentCraftRecipePrototype recipe)
    {
        if (!string.IsNullOrWhiteSpace(recipe.Category))
            return recipe.Category!;

        return recipe.Branch switch
        {
            PersistentCraftBranch.Weapon => "weapons",
            PersistentCraftBranch.Armor => "armor",
            PersistentCraftBranch.Anomaly => "anomaly",
            _ => "special",
        };
    }

    private string GetRecipeSubCategoryId(PersistentCraftRecipePrototype recipe)
    {
        if (!string.IsNullOrWhiteSpace(recipe.SubCategory))
            return recipe.SubCategory!;

        return recipe.Branch == PersistentCraftBranch.Anomaly
            ? $"tier-{recipe.Tier}"
            : string.Empty;
    }

    private string GetCategoryName(string categoryId)
    {
        var locKey = $"persistent-craft-category-{categoryId}";
        return TryLoc(locKey) ?? HumanizeIdentifier(categoryId);
    }

    private string GetSubCategoryName(string subCategoryId)
    {
        if (string.IsNullOrWhiteSpace(subCategoryId))
            return string.Empty;

        if (subCategoryId.StartsWith("tier-") && int.TryParse(subCategoryId["tier-".Length..], out var tier))
            return $"{Loc.GetString("persistent-craft-level-label")} {PersistentCraftingHelper.GetTierDisplayLabel(tier)}";

        var locKey = $"persistent-craft-subcategory-{subCategoryId}";
        return TryLoc(locKey) ?? HumanizeIdentifier(subCategoryId);
    }

    private static int GetCategoryOrder(string categoryId)
    {
        return categoryId switch
        {
            "weapons" => 0,
            "armor" => 1,
            "anomaly" => 2,
            "anomaly-detectors" => 0,
            "artifact-detectors" => 1,
            "upgrades" => 2,
            _ => 99,
        };
    }

    private static int GetSubCategoryOrder(string subCategoryId)
    {
        return subCategoryId switch
        {
            _ when subCategoryId.StartsWith("tier-") && int.TryParse(subCategoryId["tier-".Length..], out var tier) => tier,
            "starter" => 0,
            "upgrades" => 1,
            "advanced" => 2,
            "elite" => 3,
            _ => 99,
        };
    }

    private static string? TryLoc(string locKey)
    {
        try
        {
            return Loc.GetString(locKey);
        }
        catch
        {
            return null;
        }
    }

    private static string HumanizeIdentifier(string value)
    {
        if (string.IsNullOrWhiteSpace(value))
            return string.Empty;

        var spaced = value.Replace('-', ' ').Replace('_', ' ').Trim();
        return CultureInfo.InvariantCulture.TextInfo.ToTitleCase(spaced);
    }

    private string GetRecipeCategoryPath(PersistentCraftRecipePrototype recipe)
    {
        var category = GetCategoryName(GetRecipeCategoryId(recipe));
        var subCategory = GetSubCategoryName(GetRecipeSubCategoryId(recipe));

        return string.IsNullOrWhiteSpace(subCategory)
            ? category
            : $"{category} / {subCategory}";
    }

    private bool HasNodeUnlockedOrAutoAvailable(PersistentCraftState state, string nodeId)
    {
        return HasNodeUnlockedOrAutoAvailable(state, nodeId, new HashSet<string>());
    }

    private bool HasNodeUnlockedOrAutoAvailable(PersistentCraftState state, string nodeId, HashSet<string> path)
    {
        if (!_prototype.TryIndex<PersistentCraftNodePrototype>(nodeId, out var node))
            return false;

        if (state.UnlockedNodes.Contains(nodeId))
            return true;

        if (node.Cost > 0)
            return false;

        if (!path.Add(nodeId))
            return false;

        try
        {
            return node.Prerequisites.All(prerequisite => HasNodeUnlockedOrAutoAvailable(state, prerequisite, path));
        }
        finally
        {
            path.Remove(nodeId);
        }
    }

    private string BuildIngredientMarkup(PersistentCraftRecipePrototype recipe)
