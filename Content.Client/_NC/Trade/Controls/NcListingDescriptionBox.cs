using System.Numerics;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Maths;
using Robust.Shared.Utility;

namespace Content.Client._NC.Trade.Controls;

[GenerateTypedNameReferences]
public sealed partial class NcListingDescriptionBox : PanelContainer
{
    private float _layoutMaxWidth = float.PositiveInfinity;

    public NcListingDescriptionBox()
    {
        RobustXamlLoader.Load(this);
    }

    public void SetDescription(string? fullText, int maxChars)
    {
        var full = fullText ?? string.Empty;

        if (string.IsNullOrWhiteSpace(full))
        {
            Visible = false;
            Label.Visible = false;
            Label.ToolTip = null;
            Label.SetMessage(new FormattedMessage());
            return;
        }

        Visible = true;
        Label.Visible = true;

        var trimmed = TrimToChars(full, maxChars);

        var msg = new FormattedMessage();
        msg.AddText(trimmed);

        Label.SetMessage(msg);
        Label.ToolTip = full;

        UpdateMaxWidth();
    }

    public void SetLayoutMaxWidth(float width)
    {
        _layoutMaxWidth = width > 0 ? width : float.PositiveInfinity;
        UpdateMaxWidth();
        InvalidateMeasure();
    }

    protected override void Resized()
    {
        base.Resized();
        UpdateMaxWidth();
    }

    protected override Vector2 MeasureOverride(Vector2 availableSize)
    {
        SyncMaxWidth(availableSize.X);
        return base.MeasureOverride(availableSize);
    }

    protected override Vector2 ArrangeOverride(Vector2 finalSize)
    {
        SyncMaxWidth(finalSize.X);
        return base.ArrangeOverride(finalSize);
    }

    private void UpdateMaxWidth()
    {
        SyncMaxWidth(Size.X);
    }

    private void SyncMaxWidth(float candidateWidth)
    {
        var width = candidateWidth > 0 && float.IsFinite(candidateWidth)
            ? candidateWidth
            : (Size.X > 0 ? Size.X : float.PositiveInfinity);

        if (float.IsFinite(_layoutMaxWidth))
            width = float.IsFinite(width) ? MathF.Min(width, _layoutMaxWidth) : _layoutMaxWidth;

        if (width > 0 && float.IsFinite(width))
            Label.MaxWidth = MathF.Max(0, width);
    }

    private static string TrimToChars(string s, int maxChars)
    {
        if (maxChars <= 0 || s.Length <= maxChars)
            return s;

        return s[..maxChars].TrimEnd() + "...";
    }
}
